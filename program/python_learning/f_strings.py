name = 'Akshaya'
age = 27
height = 1.7
# Print by using string concatenation(+) and typecasting
print("My name is "+name+". My age is "+str(age)+" and My height is "+str(height))
# Print by using ,
print("My name is",name,". My age is",age,"and My height is ",height)
# Print by using f-string
print(f"My name is {name}. My age is {age} and My height is {height}")
print(f"My father's age is {age*2}")
print(f"sum of 5 and 4 is {5+4}")